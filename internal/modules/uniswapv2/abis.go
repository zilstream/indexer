package uniswapv2

import (
	"fmt"
	"strings"

	"github.com/ethereum/go-ethereum/accounts/abi"
)

// initializeABIs sets up the contract ABIs for parsing events
func (m *UniswapV2Module) initializeABIs() error {
	// Initialize Factory ABI
	factoryABI, err := abi.JSON(strings.NewReader(UniswapV2FactoryABI))
	if err != nil {
		return fmt.Errorf("failed to parse factory ABI: %w", err)
	}
	m.factoryABI = &factoryABI

	// Initialize Pair ABI
	pairABI, err := abi.JSON(strings.NewReader(UniswapV2PairABI))
	if err != nil {
		return fmt.Errorf("failed to parse pair ABI: %w", err)
	}
	m.pairABI = &pairABI

	// Add ABIs to event parser
	m.parser.AddContract(m.factoryAddress, &factoryABI)
	// Note: We'll add individual pair contracts as we discover them

	return nil
}

